// Server-side fake user database
let users = [
  {username: "Sunny", password: "12345", role: "admin"},
  {username: "Ravi", password: "11111", role: "user"}
];

// Server-side login function
function loginServer(inputUser, inputPass) {
  let found = users.find(u => u.username === inputUser && u.password === inputPass);

  if(found) {
    if(found.role === "admin") {
      return "Welcome Admin! You can manage users.";
    } else {
      return "Welcome User! You can see your dashboard.";
    }
  } else {
    return "Login Failed! Check username and password.";
  }
}

// Example of server usage
console.log(loginServer("Sunny", "12345")); // Welcome Admin!
console.log(loginServer("Ravi", "11111"));  // Welcome User!
console.log(loginServer("John", "0000"));   // Login Failed!





















